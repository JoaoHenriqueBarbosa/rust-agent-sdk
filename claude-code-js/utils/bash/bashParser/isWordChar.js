function isWordChar(c3) {
  return c3 >= "a" && c3 <= "z" || c3 >= "A" && c3 <= "Z" || c3 >= "0" && c3 <= "9" || c3 === "_" || c3 === "/" || c3 === "." || c3 === "-" || c3 === "+" || c3 === ":" || c3 === "@" || c3 === "%" || c3 === "," || c3 === "~" || c3 === "^" || c3 === "?" || c3 === "*" || c3 === "!" || c3 === "=" || c3 === "[" || c3 === "]";
}
